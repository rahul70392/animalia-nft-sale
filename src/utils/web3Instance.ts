export const toWei = (ether: any) => {
  return web3.utils.toWei(web3.utils.toBN(ether), 'ether')
}

export const toEther = (wie: any) => {
  return web3.utils.fromWei(wie)
}

export const BNtoNumber = (value: any) => {
  return web3.utils.toWei(value, 'ether')
}
